<h2>IRB Approval</h2>
<p>This study has been approved by the Institutional Review Board for Human Subjects at Princeton University.</p>
<h2>Study Purpose</h2>
<p>The study assesses hows individuals are exposed to, consuming, sharing, and acting on health and political information and 
misinformation during the COVID-19 pandemic.</p>
<h2>Study Benefits to Society</h2>
<p>This study will help identify information distribution pathways for truthful and inaccurate information during the COVID-19 pandemic. 
Understanding these pathways is crucial to designing public health interventions and addressing online misinformation.</p>
