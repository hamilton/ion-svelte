<h2>IRB Approval</h2>
<p>
    This study has been approved by the Institutional Review Board for
    Human Subjects at Princeton University.
</p>

<h2>Study Purpose</h2>
<p>
    The study assesses hows individuals are exposed to, consuming,
    sharing, and acting on health and political information and
    misinformation during the COVID-19 pandemic.
</p>

<h2>Study Eligibility</h2>
<p>
    All persons over the age of 18 and located in the United States are eligible to participate in the study.
</p>

<h2>Study Benefits to Society</h2>
<p>
    This study will help identify information distribution pathways for
    truthful and inaccurate information during the COVID-19 pandemic.
    Understanding these pathways is crucial to designing public health
    interventions and addressing online misinformation.
</p>

<h2>Study Procedures</h2>
<p>
    During the study, your browser will automatically send data to Mozilla
    about how you engage with health and news websites, in addition to
    standard Firefox telemetry. We have taken a number of steps to
    minimize and protect this data, which we describe below. You may also
    be asked to participate in periodic surveys. If you choose to provide
    demographic for Pioneer research purposes, that information will also
    be used for this study.
</p>

<p>
    Your participation in the study will conclude when data collection is
    completed or, if you choose to discontinue participation, when you
    make that choice. Data collection will last no longer than 6 months,
    and we will not retain data that is linkable to your browser for 
    longer than 6 months.
</p>

<h2>Leaving the Study</h2>
<p>
    You can leave the study at any time from the Pioneer preferences page
    (about:pioneer) and clicking “Leave Study.” When you leave the study,
    the study’s browser extension will be automatically uninstalled,
    removing both the study code and data. You will not be able to rejoin
    the study if you leave. If you turn off Pioneer, you will also leave
    the study.
</p>

<h2>Study Risks and Alternative Procedures</h2>
<p>
    There is minimal risk associated with participating in the study.
    There are no alternative means of participating in the study that may
    be advantageous to you.
</p>

<h2>Privacy</h2>
<p>
    We will protect the information that you provide in several ways:
    minimizing the data we collect, keeping the data we collect secure,
    and limiting the information we use and disclose.
</p>

<p>
    <em>Data minimization:</em> We will limit our data collection to
    information needed to carry out our study. We will not collect your
    name, email address, or similar contact information. We also will make
    no effort to identify you on the basis of information you provide.
    When collecting data about your web browsing, we will use coarse and
    aggregate measures as much as possible. For instance, when collecting
    data about your visit to a webpage, we will only collect a time window
    in which the event occurred instead of the exact time, and we will
    only collect the domain name and page type for the webpage rather than
    the full URL. We will also only collect activity specific to a website
    if we have previously categorized the website as health or news
    related.
</p>

<p>
    <em>Data security:</em> We will encrypt the data that you provide both
    in transit and at rest. We will only allow access to Princeton
    researchers and Mozilla personnel who have specific responsibilities
    in carrying out the study.
</p>

<p>
    <em>Data use and disclosure:</em> We will only use the data you
    provide to carry out this academic study. We will not disclose
    information that identifies you or that could reasonably be used to
    identify you.
</p>

