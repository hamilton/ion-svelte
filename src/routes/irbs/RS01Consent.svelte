<h2>Study Purpose</h2>
<p>
    The goal of this study is to begin understanding how people browse the internet, 
    where they spend their time, and how these browsing dynamics differ according to a 
    demographic survey completed in the Rally onboarding process.
</p>

<p>
    To understand browsing better we will collect <b>data about your attention</b> when you are browsing the 
    internet as well as a number of characteristics including <b>time on site, scroll depth, tab switching, 
    canonical URL, the canonical URL that brought you to the current webpage, title and description of 
    each page you are on</b>.  It will additionally collect time stamps when audio is playing in the browser, 
    but will not collect information about what audio is playing nor what video might be playing with the audio. 
    This data is not aggregated before it is sent to our servers; this study will record all events.  
    After we analyze our data we might publish aggregated insights on our blog or other channels.
    We will only publish insights that include 5000 individuals to ensure your identity is protected.
</p>

<p>
    Data will be collected through a custom browser web extension developed by the Rally team. 
    The web extension will be offered on a voluntary basis to a pool of browser users that have opted 
    in to the Mozilla Rally experience.  If you opt in to the study, we will install the web extension 
    and data will be collected through browser telemetry.  Mozilla will store the data on Google Cloud 
    where all analysis will be conducted. This server is restricted to the Rally team researchers and is encrypted. 
    Data will be transferred between the participant's browser and the Mozilla analysis database protected by TLS.
</p>

